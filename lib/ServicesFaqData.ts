// 4. Generalized Lookup Function
export function getFaqsForService(serviceSlug: string): FaqItem[] {
    return ALL_SERVICE_FAQS[serviceSlug] || [];
}

export function findServiceFaqBySlugs(serviceSlug: string, faqSlug: string): FaqItem | undefined {
    const serviceFaqs = getFaqsForService(serviceSlug);
    return serviceFaqs.find(faq => faq.slug === faqSlug);
}

// 5. Function to get ALL FAQs for generateStaticParams
// This is used for generating all static routes at build time.
export function getAllFaqRoutes(): { serviceSlug: string, faqSlug: string }[] {
    return Object.entries(ALL_SERVICE_FAQS).flatMap(([serviceSlug, faqs]) => {
        return faqs.map(faq => ({
            serviceSlug, // e.g., 'e-commerce-support-services'
            faqSlug: faq.slug // e.g., 'what-is-ecommerce-support'
        }));
    });
}

